const fs = require('fs');
const path = require('path');
const { globSync } = require('glob');

const targetPath = path.join(__dirname, '../../src/core/config/options.js');
const original = fs.readFileSync(targetPath, 'utf-8');

/**
 * Find all plugin option types from plugin files
 * @returns {Array<{name: string, typeName: string, relativePath: string}>}
 */
function findPluginOptions() {
	const files = globSync('src/plugins/**/*.js', { cwd: path.join(__dirname, '../..') });
	const plugins = new Map(); // Use Map to deduplicate by plugin name

	for (const file of files) {
		const fullPath = path.join(__dirname, '../..', file);
		const content = fs.readFileSync(fullPath, 'utf-8');

		// Match: @typedef {Object} *PluginOptions or @typedef *PluginOptions
		const match = content.match(/@typedef\s+(?:\{[^}]*\}\s+)?(\w+PluginOptions)/);

		if (match) {
			const typeName = match[1];
			// Convert PascalCase to camelCase for plugin name
			let pluginName = typeName.replace(/PluginOptions$/, '');
			pluginName = pluginName.charAt(0).toLowerCase() + pluginName.slice(1);

			// Handle special cases
			if (pluginName.length <= 3) pluginName = pluginName.toLowerCase(); // e.g., 'hR' stays 'hr'

			// Get relative path from options.js to the plugin source file
			const optionsJsDir = path.dirname(targetPath); // src/core/config
			const relativePath = path.relative(optionsJsDir, fullPath);

			// Deduplicate: prefer non-index paths (e.g., table.js over table/index.js)
			if (!plugins.has(pluginName) || !relativePath.includes('/index')) {
				plugins.set(pluginName, {
					name: pluginName,
					typeName: typeName,
					relativePath: relativePath.split(path.sep).join('/'),
				});
			}
		}
	}

	return Array.from(plugins.values()).sort((a, b) => a.name.localeCompare(b.name));
}

// Find all plugin options
const pluginOptions = findPluginOptions();

// Generate JSDoc property annotations
// Import from source .js files
const jsdocProperties = pluginOptions.map((p) => ` * @property {import('${p.relativePath}').${p.typeName}} [${p.name}]`).join('\n');

// Strategy: Find the "=== [ Plugin-Specific Options ] ===" marker and replace everything until the next "===" (3+ equals)
// This allows for flexible content replacement regardless of what's between the markers
const fullPattern = /( \* === \[ Plugin-Specific Options \] ===\n)((?:(?! \* ={3,}).)*)/s;

let wasInjected = false;

const replaced = original.replace(fullPattern, (_, markerLine) => {
	wasInjected = true;
	// Preserve the marker line, add separators and plugin properties
	return `${markerLine} * ---[ Auto-generated by scripts/check/inject-plugin-jsdoc.cjs - DO NOT EDIT MANUALLY ]---\n${jsdocProperties}\n * ///\n * ---[ End of auto-generated plugin options ]---\n`;
});

if (wasInjected) {
	fs.writeFileSync(targetPath, replaced, 'utf-8');
	console.log(`✨ inject-plugin-jsdoc: Added ${pluginOptions.length} plugin option JSDoc properties to options.js`);
} else if (replaced === original) {
	console.warn(`⚠️  inject-plugin-jsdoc: Could not find injection marker "=== [ Plugin-Specific Options ] ===" in options.js`);
}
