const fs = require('fs');
const path = require('path');

const PLUGINS_INDEX_PATH = path.join(__dirname, '../../src/plugins/index.js');
const CONSTRUCTOR_PATH = path.join(__dirname, '../../src/core/section/constructor.js');
const TYPEDEF_PATH = path.join(__dirname, '../../src/typedef.js');

/**
 * Extract plugin names from plugins/index.js export statement
 * @returns {string[]} Array of plugin names
 */
function extractPluginNames() {
	try {
		const content = fs.readFileSync(PLUGINS_INDEX_PATH, 'utf8');

		// Match the export { ... } statement
		const exportMatch = content.match(/export\s*\{([^}]+)\}/);
		if (!exportMatch) {
			console.warn('⚠️  gen-button-types: Could not find export statement in plugins/index.js');
			return [];
		}

		// Extract individual names
		const names = exportMatch[1]
			.split(',')
			.map((s) => s.trim())
			.filter(Boolean);

		return names;
	} catch (error) {
		console.error('❌ gen-button-types: Error reading plugins/index.js:', error.message);
		return [];
	}
}

/**
 * Extract command button names from _defaultButtons function in constructor.js
 * @returns {string[]} Array of command button names
 */
function extractCommandButtons() {
	try {
		const content = fs.readFileSync(CONSTRUCTOR_PATH, 'utf8');

		// Match the _defaultButtons function and its return object
		const funcMatch = content.match(/function _defaultButtons[\s\S]+?return\s*\{([\s\S]+?)\n\t\};/);
		if (!funcMatch) {
			console.warn('⚠️  gen-button-types: Could not find _defaultButtons function in constructor.js');
			return [];
		}

		// Extract all keys from the return object
		const keys = [...funcMatch[1].matchAll(/^\s*(\w+):/gm)].map((m) => m[1]);

		return keys;
	} catch (error) {
		console.error('❌ gen-button-types: Error reading constructor.js:', error.message);
		return [];
	}
}

/**
 * Update typedef.js with extracted button types using marker-based injection
 * Similar to inject-plugin-jsdoc.cjs pattern
 * @param {string[]} commands - Command button names
 * @param {string[]} plugins - Plugin names
 */
function updateTypedef(commands, plugins) {
	try {
		const original = fs.readFileSync(TYPEDEF_PATH, 'utf8');

		// Generate union types
		const commandUnion = commands.map((c) => `"${c}"`).join('|');
		const pluginUnion = plugins.map((p) => `"${p}"`).join('|');

		// Strategy: Find the "=== [ Button Types - Auto-generated ] ===" marker and replace everything until the next "===" (3+ equals)
		// This allows for flexible content replacement regardless of what's between the markers
		const fullPattern = /( \* === \[ Button Types - Auto-generated \] ===\n)((?:(?! \* ={3,}).)*)/s;

		let wasInjected = false;

		const replaced = original.replace(fullPattern, (_, markerLine) => {
			wasInjected = true;
			// Preserve the marker line, add separators and button type definitions
			return (
				`${markerLine}` +
				` * ---[ Auto-generated by scripts/check/gen-button-types.cjs - DO NOT EDIT MANUALLY ]---\n` +
				` *\n` +
				` * Default command buttons available in the toolbar\n` +
				` * @typedef {${commandUnion}} SunEditor.UI.ButtonCommand\n` +
				` *\n` +
				` * Plugin buttons available in the toolbar\n` +
				` * @typedef {${pluginUnion}} SunEditor.UI.ButtonPlugin\n` +
				` *\n` +
				` * Single button item in the toolbar (includes special controls and custom strings)\n` +
				` * @typedef {SunEditor.UI.ButtonCommand|SunEditor.UI.ButtonPlugin|SunEditor.UI.ButtonSpecial|string} SunEditor.UI.ButtonItem\n` +
				` *\n` +
				` * Button list configuration for the toolbar\n` +
				` * 2D array of button items, where each sub-array represents a button group\n` +
				` * @typedef {Array<Array<SunEditor.UI.ButtonItem>|SunEditor.UI.ButtonSpecial>} SunEditor.UI.ButtonList\n` +
				` * ///\n` +
				` * ---[ End of auto-generated button types ]---\n` +
				` */\n`
			);
		});

		if (wasInjected) {
			fs.writeFileSync(TYPEDEF_PATH, replaced, 'utf8');
			console.log(`✨ gen-button-types: Updated ButtonCommand (${commands.length} items) and ButtonPlugin (${plugins.length} items)`);
		} else if (replaced === original) {
			console.warn('⚠️  gen-button-types: Could not find injection marker "=== [ Button Types - Auto-generated ] ===" in typedef.js');
		}
	} catch (error) {
		console.error('❌ gen-button-types: Error updating typedef.js:', error.message);
	}
}

/**
 * Main execution
 */
function main() {
	const commands = extractCommandButtons();
	const plugins = extractPluginNames();

	if (commands.length === 0) {
		console.error('❌ gen-button-types: No command buttons found!');
		return;
	}

	if (plugins.length === 0) {
		console.error('❌ gen-button-types: No plugins found!');
		return;
	}

	updateTypedef(commands, plugins);
}

main();
